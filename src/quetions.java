    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here: start quiz
        if(jTextField1.getText().equals(""))
            JOptionPane.showMessageDialog(null,"Please enter Team code");
        else{
            teamCode= jTextField1.getText();
            jTextField1.setEditable(false);
            jLabel1.setText("Quetions");
            printQuestion(Q1,oa1,ob1,oc1,od1);
            Timer timer = new Timer();
            timeleft=1200;
            TimerTask task = new TimerTask(){
                public void run(){
                    jLabel2.setText(Integer.toString(timeleft)+" seconds left");
                    timeleft--;
                    if(timeleft==-1){
                        timer.cancel();
                        JOptionPane.showMessageDialog(null,"TIME OVER");
                        jButton3.doClick();
                    }
                    else if(sub){
                        timer.cancel();
                        sub=false;
                    }
                }
            };
            timer.scheduleAtFixedRate(task, 1000, 1000);
            jButton4.setVisible(false);
            jRadioButton1.setVisible(true);
            jRadioButton2.setVisible(true);
            jRadioButton3.setVisible(true);
            jRadioButton4.setVisible(true);
            jLabel3.setVisible(true);
            jLabel4.setVisible(true);
            jLabel5.setVisible(true);
            jLabel6.setVisible(true);
            jLabel7.setVisible(true);
            jButton1.setVisible(true);
            jButton2.setVisible(true);
            jButton3.setVisible(true);
            jButton6.setVisible(true);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

